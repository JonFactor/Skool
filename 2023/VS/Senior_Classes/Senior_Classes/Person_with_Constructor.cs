using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Senior_Classes
{
	internal class Person_with_Constructor
	{
		private string name;
		private int age;
		private double height;

		public string Name
		{
			get { return name; }
			set { name = value; }
		}
		public int Age
		{
			get { return age;  }
			set { age = value; }
		}
		public double Height
		{
			get { return height; }
			set { height = value; }
		}

	}
}
